    private void btnCrearEmpresaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearEmpresaMouseClicked
        try{
            if(!txtNombreEmpresa.getText().equals("") && !txtCelularEmpresa.getText().equals("")){
                String nombre = txtNombreEmpresa.getText();
                String celular = txtCelularEmpresa.getText();
                
                empresaController.crearEmpresa(nombre,celular);
                
                this.dispose();
                Inicio home = new Inicio();
                home.setVisible(true);
                home.setLocationRelativeTo(null);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Los campos no pueden estar vacios a la hora de crear la empresa..", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            System.out.println("Error al crear la empresa: "+e);
        }
    }//GEN-LAST:event_btnCrearEmpresaMouseClicked

